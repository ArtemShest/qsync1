extern uint8_t receive_buff[20];
extern t_CS CS;

void USER_UART_IRQHandler(UART_HandleTypeDef *huart)
{
    if(USART2 == huart2.Instance)                                   // если перрывание на uart2
    {
        if(RESET != __HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE))   // если прерывание по idle-флагу
        {
            __HAL_UART_CLEAR_IDLEFLAG(huart);                     // сброс флага пустой линии
            USER_UART_IDLECallback(huart);                        // вызов обработчика перрывания пустой линии
        }
    }
}


void USER_UART_IDLECallback(UART_HandleTypeDef *huart)
{
    HAL_UART_DMAStop(&huart2);

    //считаем размер принятых данных
    uint8_t data_length  = BUFFER_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart2_rx);

    if (data_length == 6)
    {
        for(uint8_t i = 0; i < data_length; i++)
        {
        	CS.buff[i] = receive_buff[i];
        }
      	CS.newMsg = 1;
    }

	//обнуляем буфер принятых данных
    memset(receive_buff,0,data_length);
    data_length = 0;

    //рестарт дма
    HAL_UART_Receive_DMA(&huart2, (uint8_t*)receive_buff, 20);
}
